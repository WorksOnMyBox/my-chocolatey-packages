$url        = 'https://www.lancom-systems.de/fileadmin/download/LANtools/LANconfig-10.40.0060-RU1.exe'

$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'EXE'
  url           = $url
  
  softwareName  = 'LANconfig*'

  checksum      = '8c2029371b86f973d419e83df5a31e1c6598adc5aaa43facb0d7c3aae6a3727b'
  checksumType  = 'sha256'
  
  silentArgs    = "/params:/silent"
  validExitCodes= @(0)
}

Install-ChocolateyPackage @packageArgs
