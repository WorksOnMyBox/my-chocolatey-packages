$url        = 'https://www.lancom-systems.de/fileadmin/download/LANtools/LANconfig-10.40.0053-Rel.exe'

$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'EXE'
  url           = $url
  
  softwareName  = 'LANconfig*'

  checksum      = 'e41a1a05990f2cd24602cf90dd731b14a3965f3bf63d5bdd38d93847bde59b7a'
  checksumType  = 'sha256'
  
  silentArgs    = "/params:/silent"
  validExitCodes= @(0)
}

Install-ChocolateyPackage @packageArgs
